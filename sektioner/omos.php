	<p>Jan købte forretningen i 2015 for at forfølge en gammel drøm om at blive selvstændig. I 2016 ændrede butikken navn fra Maabo Cykler til Aarhus Cykler, hvor god kvalitet og service fortsat er i højsædet.</p>

